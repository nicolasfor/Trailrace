public class PuntoReferencia {

    private String distanciaInicio;

    private String latitud;

    private String longitud;

    public PuntoReferencia(String latitud, String longitud, String distanciaInicio) {
        this.latitud=latitud;
        this.longitud=longitud;
        this.distanciaInicio=distanciaInicio;
    }

    public PuntoReferencia() {
    }

    public PuntoReferencia(long id, Date fechaCreado, String distanciaInicio, String latitud, String longitud) {
        this.distanciaInicio = distanciaInicio;
        this.latitud = latitud;
        this.longitud = longitud;
    }

    public String getDistanciaInicio() {
        return distanciaInicio;
    }

    public void setDistanciaInicio(String distanciaInicio) {
        this.distanciaInicio = distanciaInicio;
    }

    public String getLatitud() {
        return latitud;
    }

    public void setLatitud(String latitud) {
        this.latitud = latitud;
    }

    public String getLongitud() {
        return longitud;
    }

    public void setLongitud(String longitud) {
        this.longitud = longitud;
    }


}
