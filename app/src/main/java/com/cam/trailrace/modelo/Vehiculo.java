package com.cam.trailrace.modelo;

import java.util.Date;

/**
 * Created by nsanc on 13/09/2016.
 */
public class Vehiculo {

    private String marca;

    private String modelo;

    private String motor;

    private String nombre;

    private String tipo;

    private String potenciaMaxima;

    private String rpmMaxima;

    private String torqueMaximo;

    private String velocidadMaxima;

    public Vehiculo() {
    }

    public Vehiculo(String marca, String modelo, String motor, String nombre, String tipo, String potenciaMaxima, String rpmMaxima, String torqueMaximo, String velocidadMaxima) {
        this.marca = marca;
        this.modelo = modelo;
        this.motor = motor;
        this.nombre = nombre;
        this.tipo = tipo;
        this.potenciaMaxima = potenciaMaxima;
        this.rpmMaxima = rpmMaxima;
        this.torqueMaximo = torqueMaximo;
        this.velocidadMaxima = velocidadMaxima;
    }

    public String getTipo() {
        return tipo;
    }

    public void setTipo(String tipo) {
        this.tipo = tipo;
    }

    public String getMarca() {
        return marca;
    }

    public void setMarca(String marca) {
        this.marca = marca;
    }

    public String getModelo() {
        return modelo;
    }

    public void setModelo(String modelo) {
        this.modelo = modelo;
    }

    public String getMotor() {
        return motor;
    }

    public void setMotor(String motor) {
        this.motor = motor;
    }

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    public String getPotenciaMaxima() {
        return potenciaMaxima;
    }

    public void setPotenciaMaxima(String potenciaMaxima) {
        this.potenciaMaxima = potenciaMaxima;
    }

    public String getRpmMaxima() {
        return rpmMaxima;
    }

    public void setRpmMaxima(String rpmMaxima) {
        this.rpmMaxima = rpmMaxima;
    }

    public String getTorqueMaximo() {
        return torqueMaximo;
    }

    public void setTorqueMaximo(String torqueMaximo) {
        this.torqueMaximo = torqueMaximo;
    }

    public String getVelocidadMaxima() {
        return velocidadMaxima;
    }

    public void setVelocidadMaxima(String velocidadMaxima) {
        this.velocidadMaxima = velocidadMaxima;
    }

}
